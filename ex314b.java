public class ex314b {
    public static void main(String[] args) {
        double italRoastPrice = 9.99;
        double frenchRoastPrice = 8.99;

        System.out.println(italRoastPrice == frenchRoastPrice);
        System.out.println(italRoastPrice != frenchRoastPrice);
        System.out.println(italRoastPrice >= frenchRoastPrice);
        System.out.println(italRoastPrice <= frenchRoastPrice);
        System.out.println(italRoastPrice > frenchRoastPrice);
        System.out.println(italRoastPrice < frenchRoastPrice);
    }
}
