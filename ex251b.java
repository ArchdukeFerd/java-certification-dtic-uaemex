public class ex251b {
    public static void main(String[] args) {
        int x = 15;
        int y = 7;

        x += y; // same as x = x + y;
        System.out.println(x);

        x *= y;
        System.out.println(x);

        x -= y;
        System.out.println(x);

        x /= y;
        System.out.println(x);
    }
}
